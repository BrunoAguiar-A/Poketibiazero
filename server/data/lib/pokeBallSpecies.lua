POKEBALL_SPECIES_IDS = {}
POKEBALL_SPECIES_NAME_INDEX = {}

local function normalizeSpeciesBallName(name)
    if type(name) ~= "string" then
        return nil
    end
    local normalized = name:gsub("^%s+", ""):gsub("%s+$", ""):gsub("%s+", " ")
    if normalized == "" then
        return nil
    end
    return normalized:lower()
end

local function rebuildSpeciesBallNameIndex()
    POKEBALL_SPECIES_NAME_INDEX = {}
    for name, ids in pairs(PokeBallBySpecies) do
        local normalized = normalizeSpeciesBallName(name)
        if normalized then
            POKEBALL_SPECIES_NAME_INDEX[normalized] = ids
        end
    end
end

local function getSpeciesBallIds(pokeName)
    return PokeBallBySpecies[pokeName] or POKEBALL_SPECIES_NAME_INDEX[normalizeSpeciesBallName(pokeName)]
end

local function registerSpeciesAlias(aliasName, sourceName)
    if PokeBallBySpecies[sourceName] then
        PokeBallBySpecies[aliasName] = PokeBallBySpecies[sourceName]
    end
end

local POKEBALL_VISUAL_ID_CACHE = {}

local function isPokeballVisualServerId(itemId)
    if type(itemId) ~= "number" or itemId <= 0 then
        return false
    end

    local itemType = ItemType(itemId)
    if not itemType then
        return false
    end

    local itemName = itemType:getName()
    if type(itemName) ~= "string" or itemName == "" then
        return false
    end

    itemName = itemName:lower()
    return itemName:find("ball", 1, true) ~= nil
end

local function resolvePokeballVisualId(rawId)
    if type(rawId) ~= "number" or rawId <= 0 then
        return nil
    end

    local cached = POKEBALL_VISUAL_ID_CACHE[rawId]
    if cached ~= nil then
        return cached ~= false and cached or nil
    end

    if isPokeballVisualServerId(rawId) then
        POKEBALL_VISUAL_ID_CACHE[rawId] = rawId
        return rawId
    end

    local converted = Game.getItemIdByClientID and Game.getItemIdByClientID(rawId) or nil
    if type(converted) == "number" and converted > 0 and isPokeballVisualServerId(converted) then
        POKEBALL_VISUAL_ID_CACHE[rawId] = converted
        return converted
    end

    POKEBALL_VISUAL_ID_CACHE[rawId] = false
    return nil
end

local function normalizeSpeciesBallVisualIds()
    local convertedCount = 0
    local unresolvedCount = 0

    for pokeName, ids in pairs(PokeBallBySpecies) do
        if type(ids) == "table" then
            for _, state in ipairs({"alive", "dead"}) do
                local originalId = ids[state]
                local resolvedId = resolvePokeballVisualId(originalId)

                if originalId ~= nil and resolvedId == nil then
                    unresolvedCount = unresolvedCount + 1
                    resolvedId = originalId
                elseif resolvedId ~= nil and resolvedId ~= originalId then
                    convertedCount = convertedCount + 1
                end

                ids[state] = resolvedId
            end
        end
    end

    print(string.format("[PokeBallSpecies] Visual IDs normalizados: %d convertidos, %d sem correspondencia.", convertedCount, unresolvedCount))
end

