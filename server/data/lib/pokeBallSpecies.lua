-- IDs conhecidos do items.otb que nao representam uma pokebola usavel.
-- Quando uma especie aponta para um desses IDs, o sistema faz fallback
-- para o visual padrao da pokebola base para preservar a funcionalidade.
local POKEBALL_SPECIES_INVALID_IDS = {
    [39059] = true,
    [43473] = true,
}

for _, ids in pairs(PokeBallBySpecies) do
    if type(ids.alive) == "number" and ids.alive > 0 then
        POKEBALL_SPECIES_IDS[ids.alive] = true
    end
    if type(ids.dead) == "number" and ids.dead > 0 then
        POKEBALL_SPECIES_IDS[ids.dead] = true
    end
end

--[[
    Retorna o ballKey correto para um item de pokebola.
    Aceita tanto userdata de item quanto number (item ID).
    Usa o atributo "pokeBallKey" como fallback para IDs de especie.
]]
function getItemBallKey(item)
    local itemId = type(item) == "userdata" and item:getId() or item
    for key, value in pairs(balls) do
        if itemId == value.emptyId or itemId == value.usedOn or itemId == value.usedOff then
            return key
        end
    end
    if POKEBALL_SPECIES_IDS[itemId] and type(item) == "userdata" then
        local stored = item:getSpecialAttribute("pokeBallKey")
        if stored then return stored end
    end
    return "pokeball"
end

function rememberSpeciesBallKey(ball, ballKey)
    if type(ball) ~= "userdata" then
        return ballKey
    end

    local resolvedKey = ballKey
    if not resolvedKey or resolvedKey == "" then
        resolvedKey = getBallKey(ball)
    end

    ball:setSpecialAttribute("pokeBallKey", resolvedKey)
    return resolvedKey
end

local function getStandardBallStateId(ballKey, state)
    local ballData = balls[ballKey] or balls.pokeball
    if state == "released" or state == "dead" or state == "out" then
        return ballData.usedOff
    end
    return ballData.usedOn
end

local function isValidSpeciesBallId(itemId)
    return type(itemId) == "number" and itemId > 0 and not POKEBALL_SPECIES_INVALID_IDS[itemId]
end

function getPokeballStateTargetId(ball, state, ballKey)
    if type(ball) ~= "userdata" then
        local resolvedKey = ballKey or "pokeball"
        return getStandardBallStateId(resolvedKey, state), resolvedKey
    end

    local resolvedKey = rememberSpeciesBallKey(ball, ballKey)
    local pokeName = ball:getSpecialAttribute("pokeName")
    local ids = pokeName and getSpeciesBallIds(pokeName) or nil
    local health = tonumber(ball:getSpecialAttribute("pokeHealth")) or 0
    local wantsReleased = state == "released" or state == "dead" or state == "out"

    if ids then
        if wantsReleased or health <= 0 then
            if isValidSpeciesBallId(ids.dead) then
                return ids.dead, resolvedKey
            end
        elseif isValidSpeciesBallId(ids.alive) then
            return ids.alive, resolvedKey
        end
    end

    return getStandardBallStateId(resolvedKey, state), resolvedKey
end

function applyPokeballStateVisual(ball, state, ballKey)
    if type(ball) ~= "userdata" then
        return ballKey or "pokeball", ball
    end

    local targetId, resolvedKey = getPokeballStateTargetId(ball, state, ballKey)
    if targetId and ball:getId() ~= targetId then
        ball:transform(targetId)
    end
    print(string.format(
        "[PokeBallDebug][visual] state=%s key=%s uid=%s id=%s->%s target=%s poke=%s used=%s hasRemoveMethod=%s",
        tostring(state),
        tostring(resolvedKey),
        tostring(beforeUid),
        tostring(beforeId),
        tostring(ball:getId()),
        tostring(targetId),
        tostring(beforePoke),
        tostring(beforeUsed),
        tostring(ball.isRemoved ~= nil)
    ))
    return resolvedKey, ball
end

--[[
    Aplica o visual ALIVE na pokebola.
    So transforma o item quando existir um ID real preenchido.
]]
function applySpeciesBallAlive(ball)
    applyPokeballStateVisual(ball, "stored")
end

--[[
    Aplica o visual DEAD/OUT na pokebola.
    So transforma o item quando existir um ID real preenchido.
]]
function applySpeciesBallDead(ball)
    applyPokeballStateVisual(ball, "released")
end
